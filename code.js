// Modal Hard Skils

const openModalHard = document.querySelector(".hard-skills"); 
const modalHard =document.querySelector(".modal__Hard");
const closeModalHard = document.querySelector(".modal__close")

openModalHard.addEventListener('click', ()=>{
    event.preventDefault();
    modalHard.classList.add("modal--show")    
})
closeModalHard.addEventListener('click', ()=>{
    event.preventDefault();
    modalHard.classList.remove("modal--show")    
})


